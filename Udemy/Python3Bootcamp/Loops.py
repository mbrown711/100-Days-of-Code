# for loop syntax:
# for item in iterable_object:
#     do something with item

# iterable object = collection of data

for x in range(1, 10):
    print(x)

for letter in "coffee":
    print(letter)
    print(letter * 10)

for num in range(10):
    print(num)

for num in range(10, 20, 2):
    print(num)